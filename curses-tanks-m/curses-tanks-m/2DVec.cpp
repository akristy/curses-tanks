#include "2DVec.hpp"


Vec2D :: operator + (const Vec2D & other)
{
    Vec2D v;
    double x;
    double y;
    

    v.x = x + other.x;
    v.y = y + other.y;
 
}
Vec2D :: operator * (const Vec2D & other)
{
    Vec2D v;
    double x;
    double y;
    
    
    v.x = x * other.x;
    v.y = y * other.y;
    
}


